lista = [-10, -8, 0, 1, 2, 5, -2,-4]

temperatua_media = sum(lista) / len(lista)
print (f"A temperatura média é: {temperatua_media}°")

maior_temperatura = max(lista)
print (f"A maior temperatura é: {maior_temperatura}°")

menor_temperatura = min(lista)
print (f"A menor temperatura é: {menor_temperatura}°")